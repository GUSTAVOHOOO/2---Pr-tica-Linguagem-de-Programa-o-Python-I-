# Operadores Aritméticos
a = 10
b = 5

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
modulo = a % b


print(f"Soma: {soma}") #Soma: 15    
print(f"Subtração: {subtracao}") #Subtração: 5
print(f"Multiplicação: {multiplicacao}") #Multiplicação: 50
print(f"Divisão: {divisao}") #Divisão: 2.0
print(f"Módulo: {modulo}") #Módulo: 0   


# Operadores de Comparação
print(f"a == b: {a == b}") #a == b: False
print(f"a != b: {a != b}") #a != b: True
print(f"a > b: {a > b}") #a > b: True
print(f"a < b: {a < b}") #a < b: False
print(f"a >= b: {a >= b}") #a >= b: True
print(f"a <= b: {a <= b}") #a <= b: False

# Operadores Lógicos
x = True
y = False

print(f"x and y: {x and y}")
print(f"x or y: {x or y}")
print(f"not x: {not x}")

# Operadores de Atribuição
c = 10
c += 5
print(f"c += 5: {c}") # c += 5: 15
c -= 3 
print(f"c -= 3: {c}") # c -= 3: 12
c *= 2
print(f"c *= 2: {c}") # c *= 2: 24
c /= 4
print(f"c /= 4: {c}") # c /= 4: 6.0
c %= 3
print(f"c %= 3: {c}") # c %= 3: 0.0	


