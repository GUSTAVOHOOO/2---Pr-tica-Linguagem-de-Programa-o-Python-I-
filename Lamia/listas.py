#Listas 

lista = ["banana", "maca" , "kiwi"]
print(lista) #['banana', 'maca', 'kiwi'] 

# Tuplas

tupla = ("banana", "maca", "kiwi")  
print(tupla) #('banana', 'maca', 'kiwi')


thistuple = ("apple",)
print(type(thistuple))

#Nao é uma tupla
thistuple = ("apple")
print(type(thistuple))

#Sets
set = {"banana", "maca", "kiwi"}
print(set) #{'banana', 'maca', 'kiwi'}



# Dicionários
dict = { "nome": "GUstavo", "idade": 20} 
print(dict) #{'nome': 'GUstavo', 'idade': 20}	
