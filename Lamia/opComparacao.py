

# Definindo as variáveis a e b
a = 10
b = 5

# Operadores de Comparação
print(f"a == b: {a == b}")  # a == b: False
print(f"a != b: {a != b}")  # a != b: True
print(f"a > b: {a > b}")    # a > b: True
print(f"a < b: {a < b}")    # a < b: False
print(f"a >= b: {a >= b}")  # a >= b: True
print(f"a <= b: {a <= b}")  # a <= b: False